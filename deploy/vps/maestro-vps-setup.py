def run(cmd):
    print("\n$ " + cmd)
    r = subprocess.run(cmd, shell=True)
    if r.returncode != 0:
        print(f"ERRO: codigo {r.returncode}")
        sys.exit(r.returncode)

def write(path, content):
    os.makedirs(os.path.dirname(os.path.abspath(path)) or ".", exist_ok=True)
    with open(path, "w") as f:
        f.write(content)
    print("  + " + path)

